########## 压制 （0 为自动   1 为手动）###########
class press_hand:
    press_hand=0
    press_set=0
    press_freq=0
    
    @classmethod
    def set_press_hand(cls,press_hand):
        cls.press_hand=press_hand
    
    @classmethod
    def set_press_set(cls,press_set):
        cls.press_struct= press_set
    
    @classmethod
    
    def set_press_freq(cls,press_freq):
        cls.press_freq=press_freq
        
    